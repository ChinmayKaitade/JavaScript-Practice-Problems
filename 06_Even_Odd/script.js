// Conditional

// Write a function that checks if a number is even or odd and returns "Even" or "Odd" accordingly.

function checkEvenOrOdd(number) {
  if (number % 2 === 0) {
    console.log(`${number} is Even Number`);
  } else {
    console.log(`${number} is Odd Number`);
  }
}

checkEvenOrOdd(4);
checkEvenOrOdd(5);
checkEvenOrOdd(7);
checkEvenOrOdd(8);
