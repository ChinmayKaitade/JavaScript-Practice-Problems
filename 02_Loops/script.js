// Print the numbers from 1 to 5 to the console using loop.

// Using for-loop
for (let num = 1; num < 6; num++) {
  console.log("Using for loop:", num);
}

// Using While-loop
let i = 1;
while (i < 6) {
  console.log("Using while loop", i);
  i++;
}

// Using do-while loop
let j = 1;
do {
  console.log("Using do-while loop", j);
  j++;
} while (j < 6);
